package question2;

public class IfElseLadder {
	public static void main( String[] args ) {
		int a = 1, b = 2;

		if      ( a > b ) System.out.println( "a is greater than b" );
		else if ( a < b )	System.out.println( "a is less than b"    );
		else         			System.out.println( "a is equal to b"     );
	}
}
